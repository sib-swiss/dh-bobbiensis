        <h2 id="description" class="text-left">Brief Description of VL 1</h2>

        <p class="text-justify">
            Dated between 380 and 420 CE by Elias Avery Lowe, G.VII.15 or codex k or VL 1 or Codex Bobbiensis, is one of the
            oldest Latin manuscripts of the Gospels. It includes Mk 8:8 to Mt 15:36a on 96 folios (lac. 1 folio between 48v
            and
            49r and 4 folios between 94v and 95r). This quite small codex has folios of 18-19cm high X 15,5-16cm large.
            Counted
            as number 465 in the fourth volume of the <i>Codices Latini Antiquiores</i> (CLA), G.VII.15 has “uncertain
            origin”,
            according to Lowe (1947, 18). He continues, “Africa is suggested by two considerations: the text of k is nearest
            to
            the Gospel text used by Cyprian, an African writer, and the peculiar type of uncial in k has its nearest
            parallels
            in two fifth century MSS of Cyprian (Nos. **458, 464). It was probably at Bobbio from earliest times, and the
            survival of so unusable a book was most likely due to its being as a relic of the founder,” San Columban, a
            tradition based only on an eighteenth-century catalogue note (18). This tradition explains why G.VII.15 is also
            named “Codex Bobbiensis.” Its designation as ‘k’ comes from Constantin Tischendorf (Stampini, 1914, 335). In the
            recent digital culture, it has received the number 7820 in the <i>Leuven Database of Ancient Books</i>, now
            hosted on
            the <i>Trismegistos</i> website and referenced on papyri.info with the number 66572. Its proximity to Cyprian’s
            Matthew and
            Mark quotations has been demonstrated by von Soden (1909).
            <br><br>

            Convinced of the importance of this manuscript for NT textual criticism (NTTC) digital scholarship, the MARK16
            project has asked the <i>Institut für Neutestamentliche Textforschung</i> (INTF) to insert k in the digital
            reference
            INTF
            <i>Liste</i> and to give to it a Doc ID. It is named in this list VL 1, <i>Vetus Latina</i> 1, according to the
            collective
            name
            given to the ancient Latin versions of the Bible by Bonifatius Fischer. This naming situates VL 1 clearly in
            NTTC
            research and underlines a kind of “retour en grâce” for this codex which is often considered to be carelessly
            copied. Lowe is reticent about it, considering it as “an unusable book” (1947,18) whereas Bridget Gilfillan
            Upton
            states that its “language itself is impenetrable almost to the point of incomprehension” (2006, 197). But Bruce
            Metzger considers that “it is the most important, as regards text, of all the Old Latin copies, being
            undoubtedly
            the oldest existing representative of the African type. […] The scribe, though committing many blunders in
            writing,
            was not uneducated, for he writes with a firm and practiced hand” (1977, 315).
            <br><br>

            But thanks to a meticulous linguistic analysis, Pieter Willem Hoogterp demonstrated in 1930 that Codex
            Bobbiensis is
            a direct copy of “a very ancient archetype in cursive writing, perhaps difficult to decipher for a copyist who
            had
            only some vague notions of correct Latin” (1930, 15). This laborious copy of a third-century cursive could
            explain
            several mistakes present in VL 1 (1930, 17). Three years later, Adolophine Bakker reinforced this hypothesis by
            noticing that the scribe could make several mistakes in certain passages but in others will make none, as if he
            had
            difficulties reading the exemplar (1933, 14). With the opportunity to contemplate the Codex Bobbiensis online,
            one
            will from now discover as eyewitnesses all the particularities of this unique manuscript. It is notably known
            for
            having the shorter ending of Mark (Clivaz, 2021), but in a version slightly different from the Greek one (see
            for
            example GA 019, GA 044, GA 099 or the bilingual L1602). In the <i>conclusio brevior</i>, it notably reads qui
            “cum puero erant” instead of “qui cum Petro erant”, a reading maintained by the two correctors of VL 1. Other
            interesting
            variants are present in Mark 16:1 and 8b, as well as the addition of a full verse between 16,3 and 16,4.
        </p>


